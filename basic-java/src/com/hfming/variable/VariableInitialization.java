package com.hfming.variable;

/**
 * 变量初始化 Demo
 *
 * @author hfm
 * @version 1.1 2020-01-14
 */
public class VariableInitialization {
    public static void main(String[] args) {
        /*
         *  两种初始化方法
         */
        // 静态初始化
        int way1 = 15;
        // 动态初始胡
        int way2;
        way2 = 16;
        System.out.println(way1);
        System.out.println(way2);
    }
}
